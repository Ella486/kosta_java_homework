class Printer {
	
	void println(int pri){

		System.out.println(pri);
	}

	void println(boolean pri){
		System.out.println(pri);
	}
	
	void println(double pri){
		System.out.println(pri);
	}

	void println(String pri){
		System.out.println(pri);
	}
}
