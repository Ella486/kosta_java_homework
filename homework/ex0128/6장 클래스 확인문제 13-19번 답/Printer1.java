class Printer1 {
	
	static void println(int pri){

		System.out.println(pri);
	}

	static void println(boolean pri){
		System.out.println(pri);
	}
	
	static void println(double pri){
		System.out.println(pri);
	}

	static void println(String pri){
		System.out.println(pri);
	}
}
